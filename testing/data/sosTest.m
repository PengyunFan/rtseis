function sosTest()
  % Load the reference data
  data = load("../data/gse2.txt");
  data = double(data);
  % Set an IIR filter
  sos= [0.000401587491686, 0.000803175141692, 0.000401587491549, 1.000000000000000, -1.488513049541281,  0.562472929601870;
       1.000000000000000, -2.000000394412897,  0.999999999730209,  1.000000000000000, -1.704970593447777,  0.792206889942566;
       1.000000000000000,  1.999999605765104,  1.000000000341065,  1.000000000000000, -1.994269533089365,  0.994278822534674;
       1.000000000000000, -1.999999605588274,  1.000000000269794,  1.000000000000000, -1.997472946622339,  0.997483252685326];
  % Filter data
  yfilt = sosfilt(sos, data);
  % Write the data
  fileID = fopen('sosReference.txt','w');
  for i=1:length(yfilt)
     fprintf(fileID, '%.13e\n', yfilt(i));
  end
  fclose(fileID);
end
